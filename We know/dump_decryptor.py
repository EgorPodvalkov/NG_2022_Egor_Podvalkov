n=step+1
i=step
if step==0: # steps from 1 to 26
    n=26
    i=1
while(i<n):
    step=i
    myStr=""
    for item in cipher:
        if item.upper()>"Z" or item.upper()<"A":
            myStr+=item
            continue
        if ord(item.upper())+step>ord("Z"):
            item=chr(ord(item)+step-26)
        else:
            item=chr(ord(item)+step)
        myStr+=item
    print(str(i)+". "+myStr+"\n")
    i+=1



inputStep=int(input("Enter step(if 0, you will get all variants): "))
cipher=input("Enter cipher:\n")
# cipher="Terrgvatf, arjpbzre! Jr ner tynq gb frr, gung lbh unir fhpprffshyyl cnffrq bhe vavgvny grfg."

i=1
while True:
    if inputStep==0:
        step=i
    else:
        step=inputStep
    myStr=""
    for item in cipher:
        if item.upper()>"Z" or item.upper()<"A":
            myStr+=item
            continue
        if ord(item.upper())+step>ord("Z"):
            item=chr(ord(item)+step-26)
        else:
            item=chr(ord(item)+step)
        myStr+=item
    print("\n"+str(step)+". "+myStr)
    i+=1
    if inputStep!=0 or i==26:
        break


